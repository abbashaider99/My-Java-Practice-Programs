class stringlength{
    public static void main(String[] args){
       String len = "Java Program";
       int length = len.length();  //length find method
       System.out.println("String length: "+length);  
       System.out.println("Convert in uppercase letter:"+len.toUpperCase()); //case conversion method, same toLowerCase method.
       System.out.println("Chech wheter String is empty or not: "+len.isEmpty());
      
    }
}