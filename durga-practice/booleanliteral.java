class booleanliteral {
    public static void main(String[] args) {
        boolean b = false; // true or false (Not True or False)
        int a = 1; // required boolean "error"
        while (b) {
            System.out.println("Hi boolean b");
        }
        while (a) {
            System.out.println("Hello int a");
        }
    }
}