class excapecharacters {
    public static void main(String[] args) {
        System.out.println("Hey, My name is Haider and people call me as \"Abbas\". "); // we can use double quotes in
                                                                                        // print
        System.out.println("Hey, My name is Haider and people call me as \'Abbas\'. ");
        System.out.println("Good morning \\Good evening"); // using backslash itself in printing
    }
}